def _load():
    data = json.loads(EXAMPLE.read_text(encoding="utf-8"))
    return data["devices"]


def test_example_fixture_loads():
    snaps = _load()
    assert len(snaps) >= 10


def test_analyze_empty_fleet():
    r = analyze_fleet([])
    assert r.total == 0
    assert r.compromised_rate == 0.0
    assert r.cooccurrence == []
    assert r.outliers == []


def test_analyze_returns_report():
    r = analyze_fleet(_load())
    assert isinstance(r, FleetReport)
    assert r.total == len(_load())


def test_posture_counts_sum_to_total():
    r = analyze_fleet(_load())
    assert sum(r.posture_counts.values()) == r.total


def test_clean_devices_counted_trusted():
    r = analyze_fleet(_load())
    assert r.posture_counts["TRUSTED"] >= 3


def test_compromised_rate_between_0_and_1():
    r = analyze_fleet(_load())
    assert 0.0 <= r.compromised_rate <= 1.0
    # this fixture is mostly bad devices
    assert r.compromised_rate > 0.5


def test_device_ids_preserved():
    r = analyze_fleet(_load())
    ids = {d.device_id for d in r.devices}
    assert "clean-01" in ids
    assert "instr-01" in ids


def test_device_id_autogenerated_when_missing():
    r = analyze_fleet([{"platform": "android"}, {"platform": "android"}])
    ids = [d.device_id for d in r.devices]
    assert ids == ["device-0", "device-1"]


def test_id_alias_accepted():
    r = analyze_fleet([{"id": "alias-1", "platform": "android"}])
    assert r.devices[0].device_id == "alias-1"


def test_signal_counts_aggregate():
    r = analyze_fleet(_load())
    assert r.signal_counts.get("android.magisk.pkg", 0) >= 2


def test_category_counts_present():
    r = analyze_fleet(_load())
    assert r.category_counts.get("root", 0) >= 1
    assert r.category_counts.get("hook", 0) >= 1


def test_technique_counts_present():
    r = analyze_fleet(_load())
    assert r.technique_counts.get("T1404", 0) >= 1


def test_profile_counts_present():
    r = analyze_fleet(_load())
    assert r.profile_counts.get("emulator_farm", 0) >= 3
    assert r.profile_counts.get("dynamic_instrumentation", 0) >= 1


def test_cooccurrence_has_lift():
    r = analyze_fleet(_load())
    assert r.cooccurrence
    for a, b, c, lift in r.cooccurrence:
        assert a < b           # canonical order
        assert c >= 2          # default min_count
        assert lift > 0


def test_cooccurrence_respects_min_count():
    snaps = _load()
    r = analyze_fleet(snaps, cooccurrence_min_count=3)
    assert all(c >= 3 for (_a, _b, c, _l) in r.cooccurrence)


def test_cooccurrence_lift_above_one_for_toolkit():
    # frida lib + magisk appear together in instr devices -> lift > 1
    r = analyze_fleet(_load())
    pairs = {(a, b): lift for (a, b, c, lift) in r.cooccurrence}
    # emulator props strongly co-occur
    found = [lift for (a, b), lift in pairs.items()
             if "emu" in a and "emu" in b]
    assert found and max(found) > 1.0


def test_outliers_have_min_signals():
    r = analyze_fleet(_load(), outlier_min_signals=4)
    assert all(n >= 4 for (_d, _s, n) in r.outliers)


def test_outliers_sorted_by_signal_count_desc():
    r = analyze_fleet(_load())
    counts = [n for (_d, _s, n) in r.outliers]
    assert counts == sorted(counts, reverse=True)


def test_to_dict_shape():
    r = analyze_fleet(_load())
    d = r.to_dict()
    assert set(d) >= {
        "total", "compromised_rate", "posture_counts", "category_counts",
        "technique_counts", "profile_counts", "top_signals", "cooccurrence",
        "outliers",
    }
    assert "devices" not in d


def test_to_dict_include_devices():
    r = analyze_fleet(_load())
    d = r.to_dict(include_devices=True)
    assert "devices" in d
    assert len(d["devices"]) == r.total
    sample = d["devices"][0]
    assert {"device_id", "posture", "score", "profiles", "fired"} <= set(sample)


def test_to_dict_json_serializable():
    r = analyze_fleet(_load())
    json.dumps(r.to_dict(include_devices=True))  # must not raise


def test_top_signals_capped_and_sorted():
    r = analyze_fleet(_load())
    top = r.to_dict()["top_signals"]
    assert len(top) <= 20
    counts = [t["count"] for t in top]
    assert counts == sorted(counts, reverse=True)


def test_technique_counts_in_dict_have_names():
    r = analyze_fleet(_load())
    for t in r.to_dict()["technique_counts"]:
        assert t["id"]
        assert "name" in t


def test_device_result_dataclass():
    r = analyze_fleet([{"device_id": "z", "platform": "android",
                        "present_files": ["/system/xbin/su"]}])
    dr = r.devices[0]
    assert isinstance(dr, DeviceResult)
    assert dr.device_id == "z"
    assert dr.verdict.posture == Posture.COMPROMISED


def test_single_clean_device_zero_compromise():
    r = analyze_fleet([{"platform": "android",
                        "system_props": {"ro.build.tags": "release-keys"}}])
    assert r.compromised_rate == 0.0
    assert r.cooccurrence == []
