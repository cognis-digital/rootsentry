import pytest

from rootsentry.catalog import Platform
from rootsentry.engine import Evidence, Posture, evaluate
from rootsentry.fleet import (
    Profile, PROFILES, classify_profiles, analyze_fleet, FleetReport,
    DeviceResult,
)

EXAMPLE = Path(__file__).resolve().parent.parent / "examples" / "fleet.android.json"


# --------------------------------------------------------------------------- #
# profiles
# --------------------------------------------------------------------------- #

def _classify(**kw):
    return classify_profiles(evaluate(Evidence(platform=kw.pop("platform", Platform.ANDROID), **kw)))


def test_profiles_have_unique_ids():
    ids = [p.id for p in PROFILES]
    assert len(ids) == len(set(ids))


@pytest.mark.parametrize("p", PROFILES, ids=[p.id for p in PROFILES])
def test_profile_wellformed(p):
    assert p.severity in {"info", "elevated", "high", "critical"}
    assert p.requires_any
    assert all(isinstance(g, tuple) and g for g in p.requires_any)
    assert p.label and p.description


def test_clean_device_no_profiles():
    assert _classify(system_props={"ro.build.tags": "release-keys"}) == []


def test_dynamic_instrumentation_profile():
    hits = _classify(open_ports=[27042])
    assert any(p.id == "dynamic_instrumentation" for p in hits)


def test_rooted_and_hooked_requires_both_groups():
    only_root = _classify(present_files=["/system/xbin/su"])
    assert not any(p.id == "rooted_and_hooked" for p in only_root)
    both = _classify(present_files=["/system/xbin/su", "/data/local/tmp/frida-server"])
    assert any(p.id == "rooted_and_hooked" for p in both)


def test_emulator_farm_profile():
    hits = _classify(system_props={"ro.hardware": "goldfish"})
    assert any(p.id == "emulator_farm" for p in hits)


def test_repackaged_profile():
    hits = _classify(runtime_flags=["signature_mismatch"])
    assert any(p.id == "repackaged_app" for p in hits)


def test_evasive_root_profile():
    hits = _classify(installed_packages=["io.github.huskydg.magisk"],
                     system_props={"ro.boot.selinux": "permissive"})
    assert any(p.id == "evasive_root" for p in hits)


def test_mitm_profile_needs_both_groups():
    # user CA alone (no elevated access) should not raise mitm_analysis
    weak = _classify(runtime_flags=["user_ca_installed"])
    assert not any(p.id == "mitm_analysis" for p in weak)
    strong = _classify(present_files=["/system/xbin/su"],
                       runtime_flags=["user_ca_installed", "system_proxy_active"])
    assert any(p.id == "mitm_analysis" for p in strong)


def test_profiles_sorted_severity_first():
    hits = _classify(present_files=["/system/xbin/su", "/data/local/tmp/frida-server"],
                     installed_packages=["com.topjohnwu.magisk"], open_ports=[27042])
    order = {"critical": 0, "high": 1, "elevated": 2, "info": 3}
    sev = [order[p.severity] for p in hits]
    assert sev == sorted(sev)


def test_profile_matches_helper_and_or_semantics():
    p = Profile("x", "x", "high",
                (("a", "b"), ("c",)), "desc")
    assert p.matches({"a", "c"})
    assert p.matches({"b", "c"})
    assert not p.matches({"a"})          # missing group 2
    assert not p.matches({"c"})          # missing group 1
    assert not p.matches(set())


